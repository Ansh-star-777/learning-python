# Arthmetic Operators
print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
print(5 % 2)


# comparison operators
print(5 == 2)
print(5 == 5) # equality operator
print(5 != 2) # not equal to operator
print(5 != 5) # not equal to operator
print(5 > 2)
print(5 < 2)
print(5 < 5)
print(5 <= 5) # less than or equal to operator


# assignment operator
a = 5
print("value of a:",a)
a += 2 # this can also be written as: a = a + 2
print("new value of a:",a)
a -= 2
a *= 2
a /= 2
a %= 2
